#### Q9. write a program to enter base and height of a triangle and find its area.

#lets take inputs as a height and base

h=float(input("Enter the height of a triangle:"))

b=float(input("Enter the base of a triangle:"))

# lets calculate the area so formula is h*b/2

area=(h*b)/2

#lets display the output

print(f" the area of triangle for given base:{h} and height:{h} is {area} cm^2")