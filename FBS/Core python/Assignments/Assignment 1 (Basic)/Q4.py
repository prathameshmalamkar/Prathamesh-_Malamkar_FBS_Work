### Q.4 Write a program to enter P,T,R and calculate simple interest ?

## The perform of P,T,R to  calculate of Simple Interest

## Take input for Principal Amount
P=int(input("Enter your principal amount:"))

## Take input the Time In Years
T=int(input("Enter your time in years:"))

## Take input for the Rate Of Interest
R=int(input("Enter your rate of interest:"))

## calculate of Simple Interest Formula
SI= (P*T*R)/100

## Display Result
print(SI)
